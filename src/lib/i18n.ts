export type Translator = (key: MessageKey, values?: TranslationValues) => string;

function interpolate(template: string, values?: TranslationValues) {
  if (!values) return template;
  return template.replace(/\{(\w+)\}/g, (_, key: string) => {
    const value = values[key];
    return value === undefined ? "" : String(value);
  });
}

export function resolveLocale(value?: string | null): LocaleId {
  if (!value) return DEFAULT_LOCALE;
  const normalized = value.toLowerCase().replace("_", "-");
  const exact = SUPPORTED_LOCALES.find((locale) => locale.id === normalized);
  if (exact) return exact.id;
  const language = normalized.split("-")[0];
  return SUPPORTED_LOCALES.find((locale) => locale.id === language)?.id ?? DEFAULT_LOCALE;
}

export function createTranslator(locale: LocaleId): Translator {
  return (key, values) => interpolate(translations[locale][key] ?? en[key], values);
}

export function localeNativeName(locale: LocaleId) {
  return SUPPORTED_LOCALES.find((item) => item.id === locale)?.nativeName ?? locale;
}

